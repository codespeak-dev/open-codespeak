# ANSI color codes
class Colors:
    # Bright colors
    BRIGHT_BLUE = '\033[94m'
    BRIGHT_CYAN = '\033[96m'
    BRIGHT_GREEN = '\033[92m'
    BRIGHT_RED = '\033[91m'
    BRIGHT_YELLOW = '\033[93m'
    BRIGHT_MAGENTA = '\033[95m'

    # Text formatting
    BOLD = '\033[1m'
    DIM = '\033[2m'
    GREY = '\033[37m'

    # Reset
    END = '\033[0m'

